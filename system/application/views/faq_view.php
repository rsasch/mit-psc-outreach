		<h2 class="faq">What does the MIT Outreach Database define as outreach?</h2>
		<p>While there are different interpretations of what constitutes outreach, in the interest of practicality, we have defined it to be for purposes of this database:</p>
		<ol>
			<li>Outreach must in some way reach outwards toward the external community. This means that the program or resource is open to either
			<ol>
				<li>public participation or use or</li>
				<li>MIT participants who partner with community entities as part of the program or resource.</li>
			</ol></li>
			<li>Secondly, outreach must be active and open. Outreach of this sort can be applied for, participated in, or used by community members.</li>
			<li>Lastly, outreach must be ongoing or cyclical in some way. This excludes, for example, a one-time lecture but can include lecture series as long as it satisfies the above criteria.</li>
		</ol>
