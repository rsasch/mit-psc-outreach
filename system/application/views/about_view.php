		<h1 class="pagename">About</h1>
		<p>The MIT Public Service Center has built and maintains this database on behalf of the MIT community. We created this site to facilitate access to MIT public programming. By this means, we acknowledge the fundamental relationships that exist between MIT and the public at the local, national, and global levels. We acknowledge that this database may contain only some of the outreach programs and resources available at MIT and is not an exhaustive listing.</p>
		<h2 id="define">How we define outreach</h2>
		<p>While there are different interpretations of what constitutes outreach, in the interest of practicality, we have defined it to be for purposes of this database:</p>
		<ol>
			<li>Outreach must in some way reach outwards toward the external community. This means that the program or resource is open to either
			<ol>
				<li>public participation or use or</li>
				<li>MIT participants who partner with community entities as part of the program or resource.</li>
			</ol></li>
			<li>Secondly, outreach must be active and open. Outreach of this sort can be applied for, participated in, or used by community members.</li>
			<li>Lastly, outreach must be ongoing or cyclical in some way. This excludes, for example, a one-time lecture but can include lecture series as long as it satisfies the above criteria.</li>
		</ol>
		<h2>Mission</h2>
		<p>We seek to enable the public to enjoy the wide range of outreach programs offered by MIT. We also seek to enable the MIT community to understand that range of programming and provide a mechanism for sharing their outreach opportunities with the public.</p>
		<h2>Sponsors</h2>
		<p>This database is made possible through a donation from Tabetha and Brian Hinman and other generous donors.</p>
